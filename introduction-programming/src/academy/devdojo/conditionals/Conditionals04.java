package academy.devdojo.conditionals;

public class Conditionals04 {
    static void main(String[] args) {

// +, -, *, /,%
        double result = 10 + 20;
        System.out.println(result);
        result = 10 - 20;
        System.out.println(result);
        result = 10 * 10;
        System.out.println(result);
        result = 10 / 10;
        System.out.println(result);
        result = 20 % 2;
        System.out.println(result);
        result = 20;
        System.out.println(result);
        result = result + 50;
        System.out.println(result);
    }
}
